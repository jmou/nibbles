// Glyphs are double wide because our resolution is too low for 80 legible
// columns.

function bitmap(template: TemplateStringsArray, ...params: string[]) {
  if (template.length !== 1 || params.length !== 0) throw new Error();
  return template[0]
    .trim()
    .split("\n")
    .map((line) => line.split("").map((ch) => ch !== "."));
}

